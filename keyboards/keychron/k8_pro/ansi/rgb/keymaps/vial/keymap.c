// 1st and last layer on the cycle
#define LAYER_CYCLE_START 0
#define LAYER_CYCLE_END 6


void matrix_init_user(void) {
    set_unicode_input_mode(UC_WINC); // REPLACE UC_XXXX with the Unicode Input Mode for your OS. See table below. https://jayliu50.github.io/qmk-cheatsheet/
};


// Add the behaviour of this new keycode
bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case KC_CYCLE_LAYERS:
      // Our logic will happen on presses, nothing is done on releases
      if (!record->event.pressed) { 
        // We've already handled the keycode (doing nothing), let QMK know so no further code is run unnecessarily
        return false;
      }

      uint8_t current_layer = get_highest_layer(layer_state);
      printf("Current Layer: %d", current_layer);
      
      // Check if we are within the range, if not quit
      if (current_layer > LAYER_CYCLE_END || current_layer < LAYER_CYCLE_START) {
        return false;
      }

      uint8_t next_layer = current_layer + 1;
      if (next_layer > LAYER_CYCLE_END) {
          next_layer = LAYER_CYCLE_START;
      }

      layer_move(next_layer);
      printf("Actvating Layer: %d",next_layer);
      return false;
     
    // Process other keycodes normally
    default:
      return true;
  }
};
